def get_language(accept_language_header: str | None = None) -> str:
    if not accept_language_header:
        return DEFAULT_LANGUAGE
    normalized = accept_language_header.lower()
    for code in SUPPORTED_LANGUAGES:
        if code in normalized:
            return code
    return DEFAULT_LANGUAGE


def translate(language: str, key: str, **kwargs: str) -> str:
    lang = language if language in TRANSLATIONS else DEFAULT_LANGUAGE
    text = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS[DEFAULT_LANGUAGE].get(key) or key
    if kwargs:
        return text.format(**kwargs)
    return text
